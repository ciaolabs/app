export function LandingHeader({ isSignedIn, signInHref = "/sign-in" }: LandingHeaderProps) {
  const [theme, setTheme] = useState<"light" | "dark">("light");

  useEffect(() => {
    const htmlElement = document.documentElement;
    const initialTheme = (htmlElement.dataset.theme || "light") as "light" | "dark";
    setTheme(initialTheme);

    const observer = new MutationObserver(() => {
      const newTheme = (htmlElement.dataset.theme || "light") as "light" | "dark";
      setTheme(newTheme);
    });

    observer.observe(htmlElement, { attributes: true, attributeFilter: ["data-theme"] });

    return () => observer.disconnect();
  }, []);

  const ctaClassName =
    "clay-button-hover inline-flex h-10 items-center justify-center rounded-full border border-black bg-(--accent-blue) px-4 text-sm font-semibold text-(--selected-contrast) shadow-(--shadow-soft) sm:h-11 sm:px-5";

  const navLinks: { href: string; label: string; color: string; external?: boolean }[] = [
    { href: "#surveys", label: "Surveys", color: "#f97316" },
    { href: "#dashboards", label: "Dashboards", color: "#facc15" },
    { href: "#chat", label: "Chat", color: "#ec4899" },
  ];

  return (
    <header className="relative flex w-full items-center justify-between gap-2 px-4 py-4 sm:gap-4 sm:px-6 lg:px-8">
      <style dangerouslySetInnerHTML={{ __html: `
        @keyframes ciaoHandWaveHeader {
          0%   { transform: rotate(0deg); }
          10%  { transform: rotate(14deg); }
          20%  { transform: rotate(-8deg); }
          30%  { transform: rotate(14deg); }
          40%  { transform: rotate(-4deg); }
          50%  { transform: rotate(10deg); }
          60%  { transform: rotate(0deg); }
          100% { transform: rotate(0deg); }
        }
        .ciao-header-wave {
          animation: ciaoHandWaveHeader 1.8s ease-in-out infinite;
          transform-origin: 70% 70%;
          display: inline-block;
        }
      ` }} />

      <Link href="/" aria-label="Ciao! home" className="flex shrink-0 items-center gap-2">
        {/* eslint-disable-next-line @next/next/no-img-element */}
        <img
          src={theme === "dark" ? "/ciao-sparkle-dark.svg" : "/ciao-sparkle.svg"}
          alt=""
          aria-hidden="true"
          className="ciao-header-wave h-9 w-9 object-contain sm:h-11 sm:w-11"
        />
        {/* eslint-disable-next-line @next/next/no-img-element */}
        <img
          src="/ciao-text.png"
          alt="Ciao!"
          className="h-7 w-auto sm:h-9"
          style={{
            filter: theme === "dark" ? "invert(1)" : "none",
          }}
        />
      </Link>

      <nav
        aria-label="Sections"
        className="pointer-events-none absolute left-1/2 hidden -translate-x-1/2 items-center gap-7 md:flex lg:gap-10"
      >
        {navLinks.map((link) => (
          <a
            key={link.label}
            href={link.href}
            {...(link.external ? { target: "_blank", rel: "noreferrer" } : {})}
            className="pointer-events-auto inline-flex items-center gap-2.5 text-base font-semibold text-(--ink) transition hover:opacity-80"
          >
            <span
              aria-hidden="true"
              className="inline-block size-2 rounded-full"
              style={{ backgroundColor: link.color }}
            />
            {link.label}
          </a>
        ))}
      </nav>

      <nav className="flex items-center gap-2 sm:gap-3">
        <a
          href={routes.docs()}
          target="_blank"
          rel="noreferrer"
          className="clay-button-hover flex h-10 w-10 items-center justify-center rounded-full border border-(--line-strong) bg-(--surface-panel-strong) text-(--ink) shadow-(--shadow-soft) sm:h-11 sm:w-11"
          aria-label="Documentation"
        >
          <BookIcon />
        </a>
        <Link
          href={routes.chat}
          className="clay-button-hover flex h-10 w-10 items-center justify-center rounded-full border border-(--line-strong) bg-(--surface-panel-strong) text-(--ink) shadow-(--shadow-soft) sm:h-11 sm:w-11"
          aria-label="Open the Ciao app"
        >
          <CiaoIcon />
        </Link>
        {isSignedIn ? (
          <StartSurveyButton className={ctaClassName}>
            <span className="sm:hidden">Start →</span>
            <span className="hidden sm:inline">Start a survey →</span>
          </StartSurveyButton>
        ) : (
          <a href={signInHref} className={ctaClassName}>
            <span className="sm:hidden">Sign in →</span>
            <span className="hidden sm:inline">Sign in to start →</span>
          </a>
        )}
      </nav>
    </header>
  );
}
